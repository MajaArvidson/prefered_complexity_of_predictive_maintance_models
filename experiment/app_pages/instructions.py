import streamlit as st # type: ignore
from utils.navigation import set_page

def instructions_page():
    st.title("AI Decision Experiment Instructions")

    st.header("Overview")
    st.write("""
    In this experiment, you will see a series of **system scenarios** based on telemetry metrics. Each scenario shows:
    - **Telemetry data** (system performance metrics)
    - **An AI recommendation** (what the system suggests you do)
    - **AI confidence** (how confident the system is in its recommendation)
    - **An explanation** of the AI's reasoning
    """)

    st.header("Telemetry Variables")
    st.write("""
    The following variables describe the system state in each scenario:
    """)

    st.dataframe({
        "Variable": ["CPU Usage (%)", "Memory Usage (%)", "Disk I/O (MB/s)", "Network Latency (ms)"],
        "Description": [
            "Percentage of CPU being used",
            "Percentage of RAM being used",
            "Disk read/write activity in MB/s",
            "Time for network requests in milliseconds"
        ],
        "Values in this experiment": [
            "1 - 100",
            "22 - 98",
            "1 - 485",
            "5 - 490"
        ]
    }, hide_index=True)

    st.header("AI Models")
    st.write("""
    You will see scenarios generated by **two AI models**:

    1. **High-complexity, high-accuracy model**
       - More precise recommendations
       - Harder to interpret
    2. **Low-complexity, lower-accuracy model**
       - Simpler recommendations
       - Easier to understand

    The order of scenarios is **randomized** so you will experience a mix of both models.
    """)

    st.header("Your Task per Scenario")
    st.write("""
    1. Review the **telemetry data** carefully.
    2. Examine the **AI recommendation, confidence, and explanation**.
    3. Answer the following questions:
       - How much do you **trust** the AI recommendation? (1-7)
       - How well do you **understand** the AI's decision? (1-7)
       - How **comfortable** are you relying on the AI system? (1-7)
       - Would you **follow the recommendation**? (Yes/No)
    4. Click **Next Case →** to continue to the next scenario.
    """)

    st.header("Why the Variables Matter")
    st.write("""
    The telemetry values span a range of realistic system states:

    - **CPU Usage** - low: idle, medium: normal tasks, high: heavy computation
    - **Memory Usage** - low: plenty of RAM, high: near capacity
    - **Disk I/O** - low: minimal disk activity, high: heavy reads/writes
    - **Network Latency** - low: fast, high: congested or slow network

    By varying these metrics, the experiment measures how users respond to AI recommendations under **different system stress levels**.
    """)

    st.divider()

    if st.button("Start Experiment"):
        set_page("cases")